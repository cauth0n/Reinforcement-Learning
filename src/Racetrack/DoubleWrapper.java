package Racetrack;

public class DoubleWrapper {
	private double one;
	private int two;
	
	public DoubleWrapper(double one, int two){
		this.one = one;
		this.two = two;
	}

	public double getOne() {
		return one;
	}

	public void setOne(double one) {
		this.one = one;
	}

	public int getTwo() {
		return two;
	}

	public void setTwo(int two) {
		this.two = two;
	}
	

}
